    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try{
            // Buat File .txt
            BufferedWriter out=new BufferedWriter(new FileWriter("appHari.txt"));
            // Simpan ke File
            out.write(hasil.getText());
            // Menampilkan teks jika berhasil
            JOptionPane.showMessageDialog(null, "Berhasil Simpan File");
            out.close();
            System.out.println("Current Working Directory: " + System.getProperty("user.dir"));
        }catch(IOException ex){
            JOptionPane.showMessageDialog(null, "Gagal Simpan File, error di : "+ex);
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

