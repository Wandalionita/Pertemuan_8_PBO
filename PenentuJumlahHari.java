    private void HitungHari() {
        if (tahunfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tahun belum diisi. Mohon isi tahun terlebih dahulu.", "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int thn = Integer.parseInt(tahunfield.getText());
        String selectedBulan = bulanfield.getSelectedItem().toString();

        int jumlah_hari;
        jumlah_hari = HitungHari.hitung(thn, selectedBulan);

        hasil.setText("Jumlah Hari Pada Bulan "+ bulanfield.getSelectedItem()+" Tahun "+tahunfield.getText()+" Adalah "+jumlah_hari);

    }

